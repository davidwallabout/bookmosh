export const iconDataUrl = (svg) => `data:image/svg+xml;utf8,${encodeURIComponent(svg)}`

export const getProfileAvatarUrl = (user) => {
  if (!user) return null
  if (user.avatar_url) return user.avatar_url
  const iconId = user.avatar_icon
  const icon = PROFILE_ICONS.find((i) => i.id === iconId) ?? PROFILE_ICONS[0]
  return iconDataUrl(icon.svg)
}
